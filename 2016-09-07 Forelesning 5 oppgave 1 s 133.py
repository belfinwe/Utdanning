tall = float(input('Skriv inn tall, fra 1 tilogmed 7: '))

if tall < 1:
    print('Tallet er ikke fra 1 tilogmed 7!')
else:
    if tall == 1:
        print('Mandag.')
    else:
        if tall == 2:
            print('Tirsdag.')
        else:
            if tall == 3:
                print('Onsdag.')
            else:
                if tall == 4:
                    print('Torsdag.')
                else:
                    if tall == 5:
                        print('Fredag.')
                    else:
                        if tall == 6:
                            print('Lørdag.')
                        else:
                            if tall == 7:
                                print('Søndag.')
                            else:
                                print('Tallet er ikke fra 1 tilogmed 7!')


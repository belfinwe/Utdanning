# Program som tar imot tre resultater/verdier og regner ut gjennomsnittet av de tre.

res1 = float(input('Skriv inn resultat #1: ')) # Resultatene skrives inn/input.
res2 = float(input('Skriv inn resultat #2: '))
res3 = float(input('Skriv inn resultat #3: '))

# Alle tre verdiene skal være skrevet inn, klar til å regne ut gjennomsnittet av de tre.

snitt = (res1 + res2 + res3) / 3 # Regner ut gjennomsnittet av de tre resultatene.

print('Gjennomsnittet av', res1, ',', res2, 'og', res3, 'er:', snitt) # Viser gjennomsnittet/output.


